Obstacle::Obstacle(Vector2 position)
{
    for (unsigned int row = 0; row < grid.size(); row++)
    {
        for (unsigned int col = 0; col < grid[0].size(); col++)
        {
            if (grid[row][col] == 1)
            {
                float pos_x = position.x + col * 3;
                float pos_y = position.y + row * 3;
                Block block = Block({pos_x, pos_y});
                blocks.push_back(block);
            }
        }
    }

}

std::vector<Obstacle> Obstacle::CreateObstacles()
{
    std::vector<Obstacle> obstacles;
    
    int obstacleWidth = Obstacle::grid[0].size() * 3;
    float gap = (GetScreenWidth() - 4 * obstacleWidth) / 5;

    for (int i = 0; i < 4; i++)
    {
        float offset_x = (i + 1) * gap + i * obstacleWidth;
        obstacles.push_back(Obstacle({offset_x, float (GetScreenHeight() - 200)}));
    }

    return obstacles;
}

void Obstacle::Draw()
{
    for (auto& block : blocks)
    {
        block.Draw();
    }

}